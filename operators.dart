void main() {
  var a = 10;
  var b = 4;
  // ### Aritmatika
  // penjumlahan
  var penjumlahan = a + b;
  // pengurangan
  var pengurangan = a - b;
  // perkalian
  var perkalian = a * b;
  // pembagian
  var pembagian = a / b;
  // modulus
  var modulus = a % b;

  // ### Equality & relational operators
  print('=== Equality & relational operators');
  print(a > b);
  print(a < b);
  print(a >= b);
  print(a <= b);
  print(a != b);
  print(a == b);

  // Logical operator
  print('=== Logical operator');
  var x = true;
  var y = false;

  print(x && y);
  print(x || y);
  print(!x);
}
